fun main(args: Array<String>) {

    var num1 = 15

    val num2 = 10

    val sum = num1 + num2

    println("sum = $sum")

    val sum2 = num1.plus(num2)

    println("sum2 = $sum2")

    val diff = num1 - num2

    println("diff = $diff")

    val diff2 = num1.minus(num2)

    println("diff2 = $diff2")

    num1 ++

    println("num1 = $num1")

    num1.inc()

    println("num1 = $num1")



}